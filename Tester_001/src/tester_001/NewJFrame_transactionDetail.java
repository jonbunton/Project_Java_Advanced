    public void loadTabel(){
        String[] title ={"CATEGORY","PRICE", "WEIGHT","SUBTOTAL"};
        tm = new DefaultTableModel(title,0);
        jTable2.setModel(tm);
        jTable2.setDefaultEditor(Object.class, null);
        try {
            
            ps = con.prepareStatement("SELECT * FROM DTRANS WHERE IDHTRANS=?");
            ps.setInt(1,frmReport.idtransaksi);
            rs = ps.executeQuery();
            while (rs.next()) {                
                DTrans dtranss = new DTrans(rs.getString("KATEGORY"),
                    rs.getInt("HARGA"),
                    rs.getInt("BERAT"),
		    rs.getInt("HARGA")*rs.getInt("BERAT"));
                arr.add(dtranss);
            }
            rs.close();
        } catch (Exception e) {
            Logger.getLogger(NewJFrame_transactionDetail.class.getName()).log(Level.SEVERE, null, e);
        }
        
        for(DTrans d : arr){
            
            tm.addRow(d.getObj());
            System.out.println(d.getObj());
        }
        jTable2.setDefaultEditor(Object.class, null);
        jTable2.setModel(tm);
    }

