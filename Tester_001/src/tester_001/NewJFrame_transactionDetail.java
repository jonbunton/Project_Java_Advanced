public void writeFile(File f){
        try {
            FileWriter fw = new FileWriter(f);
            BufferedWriter writer = new BufferedWriter(fw);
            writer.write("TRANSACTION DETAILS");
            writer.newLine();
            writer.newLine();
            writer.write("TRANSACTION ID : " + frmReport.idtransaksi);
            writer.newLine();
            writer.write("CATEGORY~PRICE~WEIGHT~SUBTOTAL");
            writer.newLine();
            for(DTrans dt :arr){
                writer.write(dt.toString());
                writer.newLine();
            }
            //HARUS URUT
            writer.close();
            fw.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
